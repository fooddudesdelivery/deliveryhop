Should I tip the driver?</h4>
<p class="style1">
Gratuity is not included.  A gratuity comparable to what you would tip inside a restaurant is appreciated.
</p>
<h4>
Can I order from a restaurant outside my delivery area?</h4>
<p class="style1">
Out of area deliveries are available with a $75 minimum order, 15% gratuity and advance notice.  Please <a href="https://staging.fooddudesdelivery.com/index.php?main_page=contact_us" target="_blank" class="style2">Contact us</a> to place an order.
</p>
<h4>
Can I cancel my order?</h4>
<p class="style1">
Orders cannot be cancelled once they have been placed with the restaurant. If you need to cancel or make changes to an advanced order, please <a href="https://staging.fooddudesdelivery.com/index.php?main_page=contact_us" target="_blank" class="style2">contact</a> a customer service representative.
</p>
<h4>
How do I redeem a gift certificate?</h4>
<p class="style1">
To redeem a gift certificate code login to the <a href="https://staging.fooddudesdelivery.com/index.php?main_page=account" target="_blank" class="style2">my account</a> page and enter code in the redeem gift certificate box. Once the code has been redeemed apply amount in the gift certificate box during checkout. A customer account is required to use a gift certificate.
</p>
<h4>
How do I redeem a coupon code?</h4>
<p class="style1">
Enter redemption code in the discount box during checkout. A customer account is required to use a coupon code.
</p>
<h4>
How do I use a gift card?</h4>
<p class="style1">
Enter gift card number in the gift card box during checkout. A customer account is required to use a gift card.
</p>
<h4>
How do I redeem a rewards points?</h4>
